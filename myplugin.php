<h3><a href="#">Smak</a></h3>
<div>
<p>Smaksinnet är förmågan att förnimma kemiska substanser via receptorer på tungan. Det finns åtminstone fem 
typer av sådana receptorer, smaklökar, som reagerar på respektive sött, salt, surt, beskt och umami. Umami blev 
teoretiserad 1908 och dess existens bekräftades 2000[2]. Till följd av antalet smakreceptorer menar en del anatomister 
att de i själva verket utgör fem eller eventuellt fler olika sinnen, ifall information förmedlas av olika receptorer 
till något olika områden av hjärnan. Smaksinnet är det ena av de två grundläggande kemiska sinnena och arbetar jämfört 
med luktsinnet på kort avstånd och med relativt icke-flyktiga substanser. Avsaknad av smaksinne benämns ageusi.</p>
