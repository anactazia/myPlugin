<h4>
Analoga med mänskliga sinnen</h4>

<p>Andra organismer har receptorer för att uppfatta världen omkring dem, innefattande många av sinnena listade 
ovan för människor. Emellertid varierar mekanismerna och förmågorna stort.</p>

<p>Lukt: Bland icke-mänskliga arter, har hundar ett mycket skarpare luktsinne än människor, även om mekanismen 
är liknande. Insekter har olfaktoreceptorer på sina antenner.</p>
<p>Syn: Näsgropsormar och en del boor har organ som gör det möjligt för dem att upptäcka infrarött ljus, 
så att de här ormarna kan uppfatta kroppsvärmen hos sitt byte. Vampyrarten Desmodus rotundus kan också ha 
en infraröd sensor på sin nos.[4] Infraröda sinnen, är emellertid bara syn inom ett annorlunda ljusfrekvensomfång. 
För människor börjar föremål glöda vid 500 C, medan det hos ett djur med infrarödsyn kan börja glöda redan vid 
30 C. Det har upptäckts att fåglar och en del andra djur är tetrakromater och har förmågan att se i ultraviolett
ner till 300 nanometer. Bin är också kapabla att se i ultraviolett.</p>
<p>Balanssinne: Kammaneter har en balansreceptor (en statocyst) som fungerar mycket annorlunda jämfört med 
däggdjurens halvcirkulära kanaler.</p>

<h4>Icke-analoga med mänskliga sinnen</h4>

<p>Förutom detta har en del djur sinnen som människor inte har, innefattande de följande:</p>

    <p>Elektroception (eller "elektroreception"), det mest signifikanta av de icke-mänskliga sinnena, är förmågan
    att upptäcka elektriska fält. Ett flertal benfiskar, broskfiskar och kloakdjur har utvecklat förmågan att känna 
    av förändringar i elektriska fält i sin omedelbara närhet. En del fiskar känner passivt förändring nära elektriska 
    fält; en del genererar sina egna svaga elektriska fält, och känner av mönstret hos fältmöjligheter över sin 
    kroppsyta; och en del använder dehär elektriska fältgenererande och uppfattande förmågorna för social kommunikation. 
    Mekanismerna genom vilka elektroceptiva fiskar bildar en spatial representation från väldigt små skillnader i 
    fältpotentialer involverar jämförelser av taggbundenheter från olika delar av fiskkroppar. Den enda ordningen 
    av däggdjur som veterligen uppvisar elektroception är kloakdjur. Bland dessa däggdjur har näbbdjuret det mest 
    välutvecklade sinnet för elektroception. [5] Body modification entusiaster har experimenterat med magnetiska 
    implantat för att försöka kopiera detta sinne[6], emellertid kan människor (och troliga andra däggdjur) i allmänhet 
    upptäcka elektriska fält bara indirekt genom att spåra effekterna de har på hår. En elektriskt laddad ballong, till 
    exempel, kommer att utöva en kraft på hår på en människas arm, vilket kan kännas genom känsel och identifieras som 
    kommandes från en statisk laddning (och inte från vind eller liknande). Det här är emellertid inte elektroception 
    eftersom det är en post-sensorisk kognitiv effekt.</p>
    <p>Sonar är förmågan att avgöra orientering i förhållande till andra objekt genom tolkning av reflekterat ljud. 
    Fladdermöss och valar är kända för att ha den här förmågan, även om också andra djur använder den. Den används 
    oftast för att navigera under dåliga ljusförhållanden eller för att identifiera och spåra upp byten. För tillfället 
    är det osäkert om det här helt enkelt är en högt utvecklad post-sensorisk tolkning av auditiva perceptioner eller i 
    själva verket utgör ett skilt sinne. Lösning till frågeställningen kommer att kräva hjärnröntgen av djur samtidigt 
    som de faktiskt utför ekolokation, en uppgift som har visat sig vara svår att utföra i praktiken. Blinda människor 
    rapporterar att de kan navigera genom att tolka reflekterade ljud (speciellt sina egna fotsteg), ett fenomen som är 
    känt som mänsklig ekolokation.</p>
    Magnetoception (eller "magnetoreception") är förmågan att upptäcka fluktuationer i magnetiska fält och iakttas 
    mest allmänt hos fåglar, även om det också har observerats hos insekter som bin. Även om det inte råder någon tvist
    om att det här sinnet finns hos många fåglar (det är essentiellt för navigationsförmågorna hos flyttfåglar), är det 
    inte ett välförstått fenomen.[7] Det finns experimentella och fysiska bevis som talar för att det sinnet finns i svag 
    form hos människor. Magnetotaktiska bakterier bygger miniatyrmagneter inuti sig själva och använder dem för att avgöra 
    sin orientering i förhållande till Jordens magnetfält.</p>
    <p>Tryckupptäckande använder sidolinjeorganet, som är ett tryckkänsligt system av hår återfunnet hos fiskar och en 
