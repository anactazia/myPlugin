<h3><a href="#">Andra sinnen</a></h3>
<div>
<p>Termoception är sinnet för uppfattning av värme och kyla, genom huden, bland annat med interna hudgångar. Det 
finns till viss del olika åsikter om hur många sinnen det här i själva verket handlar om, eftersom termoceptorerna 
i huden är ganska olika i jämförelse med homeostatiska termoceptorer som reagerar på invärtes kroppstemperatur.</p>

<p>Nociception innebär kroppens uppfattande av det som senare blir medvetet som smärta. Det kan klassificeras, 
beroende på metod för detsamma, som ett, två eller tre sinnen. Det finns tre typer av smärtreceptorer: kutana 
(i huden), somatiska (i leder och benhinnor) och viscerala (i organkapslar, exempelvis hjärnhinnan och hjärtsäcken ). 
Länge trodde man att smärta var att tryckreceptorer överbelastades, men forskning under 1900-talets första hälft kom 
fram till att smärta är ett skilt fenomen som sammanflätas med alla andra sinnen, också känsel. Nuförtiden menas 
smärta enligt vetenskapen vara en helt och hållet subjektiv upplevelse.</p>

<p>Proprioception, det kinestetiska sinnet, är sinnet för uppfattning av den egna kroppen med receptorer som 
återfinns i muskler, leder och senor. Dess sensorer registrerar kontinuerligt hudens, musklers och senors 
sträckning och spänning så att centrala nervsystemet har information om var kroppsdelarna befinner sig. Det är 
ett sinne som inte är väldigt känt bland människor utanför forskningsvärlden, men som folk sätter stor tillit 
till. Proprioception är att ständigt, men i visst avseende omedvetet, vara medveten om var de olika delarna av 
kroppen finns. Proprioception och känsel hör samman på små områden, och om de försvagas blir resultatet att 
perception och handlande får överraskande och djupa brister.[3] Däremot har en bläckfisk ingen eller begränsad 
proprioception på grund av de komplicerade former som deras tentakler kan forma.</p>

<p>Interoceptorer är sinnet för avkänning av den egna kroppens inre fysiologi. I kroppen finns sensorer som 
kontinuerligt mäter blodsocker, blodtryck, kroppstemperatur och liknande fysiologiska variabler.</p>

