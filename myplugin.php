<h3><a href="#">Känsel</a></h3>
<div>
<p>Känseln är förmågan förnimma mekaniskt tryck på huden, och receptorer för det finns över hela kroppen. Det finns 
många tryckreceptorer som reagerar på växlingar i tryck. Efter uppövning av känsel i fingrarna kan ett finger registrera 
en 0,0005 mm hög upphöjning. Känsel kan också ses som det sinne som har störst inverkan på en persons hälsa. Om man inte 
får tillräckligt med beröring skadas det mentala välbefinnandet. Smärta och temperatur kan räknas som att det känns av 
känselsinnet, men också att det uppfattas av nociception respektive termoception. Avsaknad eller nästan total avsaknad 
av känselsinne benämns parestesi.</p>
