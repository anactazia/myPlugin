</div>

<h3><a href="#">Lukt</a></h3>
<div>
Luktsinnet är det andra primära sinnet för att uppfatta kemiska substanser genom att signaler från hundratals olika 
luktreceptorer som var och en binder till bestämda molekylära karakteristika kombineras. Jämfört med smaksinnet 
reagerar luktsinnet på större avstånd och på flyktigare substanser. Om man inte ser de olika smaksinnena som separata 
sinnen kan man på samma sätt påstå att smaksinne och luktsinne skulle betraktas som ett sinne. Det finns många särdrag 
hos luktmolekyler, vilka sålunda retar specifika receptorer olika starkt. Olfaktorsystemet behandlar lukt i hjärnan. 
Olfaktorreceptorneuroner i näsan dör och regenereras regelbundet, vilket gör dem olika de flesta andra neuroner. 
Avsaknad av luktsinne benämns anosmi.</p>
