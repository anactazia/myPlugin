<h3><a href="#">Syn</a></h3>
<div>
<p>Syn är förmågan att förnimma elektromagnetisk strålning, ljus, inom vissa frekvensområden med ögat. 
Det finns de som menar att detta i själva verket är två eller tre sinnen. Den vanligaste åsikten bland 
neuroanatomister är att det handlar två sinnen, under förutsättning att det är olika receptorer som hanterar 
uppfattningen av färg, det vill säga ljusets fotonfrekvens, och klarhet, det vill säga amplitud/intensitet – 
ljusets fotonantal. Det har framförts att stereopsi, djupperception, också är ett skilt sinne, men det betraktas
vanligtvis som en kognitiv, alltså postsensorisk, hjärnfunktion som ska ge ny information från sensoriskt tillfört
stoff. Det finns forskare som nyligen har velat koppla samman det här är "tredje" sinnet med tidsdimensionen. 
Något noterat bevis för detta finns ändå inte ännu. Avsaknad av synsinne benämns blindhet.</p>
