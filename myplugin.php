<h3><a href="#">Hörsel</a></h3>
<div>
<p>Hörsel är förmågan att förnimma ljudvågor som fortplantar sig genom till exempel luft eller vatten. Detta får 
trumhinnan att vibrera och vibrationen fortplantar sig till innerörat. Beroende på vilken frekvens ljudet har 
aktiverar det en motsvarande grupp hårceller i snäckan. Vilka frekvenser som kan uppfattas står i omvänd proportion 
till snäckans storlek, sålunda kan vuxna människor uppfatta mellan 20 och 20000 Hz, medan småbarn och mindre djur 
kan uppfatta betydligt högre frekvenser. Via de båda hörselnerverna går signalerna till hjärnstammen som mäter 
tidsskillnaden mellan dem för att avgöra från vilken riktning ljudet kommer. Hjärnstammen skickar vidare signalerna 
till båda hjärnhalvorna.<br />Även känselsinnet kan registrera vibrationer, och är det enda sättet på vilket hörbara 
mycket låga frekvenser kan upptäckas. Avsaknad av hörsel benämns dövhet.</p>
