<h3><a href="#">Balans</a></h3>
<div>
<p>Balanssinnet, det vestibulära sinnet, är sinnet för förnimmelse av position eller acceleration och uppfattas genom 
håligheter i innerörat som innehåller vätska. Det är uppdelat i två delar, det ena kan kallas accelerationssinnet, 
eftersom det mäter den acceleration huvudet utsätts för, till exempel gravitationen. Den andra registrerar huvudets 
förhållande till lodlinjen med hjälp av så kallade otolitorgan. Forskarna är inte ense om att det här också innefattar 
sinnet för orientering. Hjärnstammen justerar reflexmässigt ögonmusklerna för att stabilisera bilden. En allmän 
uppfattning är ändå att ”riktning” är en postsensorisk kognitiv hjärnfunktion.</p>
