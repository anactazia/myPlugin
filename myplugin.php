<h3><a href="#">Sinnena och intelligens</a></h3>
<div>
<p>Howard Gardner utgick ifrån forskning om hur kreativitet tar sig uttryck hos olika individer vid sin beskrivning av 
de multipla intelligenserna: visuell, musikalisk, logisk/matematisk, lingvistisk, rörelse, naturalistisk (upptäckt senare 
än de övriga), kinestetisk, intrapersonell och interpersonell intelligens. Största delen av dehär svarar mot bestämda 
sinnen och om Rudolf Steiners högre sinnen gällande språk, tänkande och ego tas med gäller detta faktiskt alla
intelligenserna. Det är möjligt att andra sinnen (smak, lukt) likaså kan kopplas till skilda intelligenser, Gardner har 
sagt att hans sökande efter ytterligare intelligenser fortsätter. Sålunda tycks intelligens och sensorisk perception 
vara nära relaterade till varandra. Guy Murchie har tänkt sig att det kan finnas över 30 sinnen, varibland tidssinne 
och sinne för rädsla kan nämnas.</p>
